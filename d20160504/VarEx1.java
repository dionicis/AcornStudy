package d20160504;

public class VarEx1 {
	public static void main(String[] args) {
		// 변수
		// 자료형 변수명
		byte b1; //b1이라는 이름의  byte크기 변수 선언
		
		b1 = 10; //b1변수에 10의 값을 대입
		System.out.println(b1);//b1변수값 출력
		
		b1 = 20; //b1변수에 20의 값을 대입
		System.out.println(b1);//b1변수값 출력
	}//main end
}//class end
