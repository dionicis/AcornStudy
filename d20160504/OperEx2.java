package d20160504;

//2순위 연산자:산술연산자.
public class OperEx2 {
	public static void main(String[] args) {
		int a = 27; //int형 자료형 a을 선언하고 100을 넣음
		int b = 10; //int형 자료형 b을 선언하고 100을 넣음
		
		System.out.println(a+b); //두 수의 합을 구함
		System.out.println(a-b); //두 수의 차을 구함
		System.out.println(a*b); //두 수의 곱을 구함
		System.out.println(a/b); //두 수의 몫을 구함
		System.out.println(a%b); //두 수의 나머지을 구함 
	}
}
